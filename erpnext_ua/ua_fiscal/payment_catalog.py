def _row(
	name: str,
	channel: str,
	code: int,
	allow_cashless: bool,
	allow_other: bool,
	allow_prepayment: bool,
	allow_debt: bool,
	*,
	form: str | None = None,
	requires_terminal: bool = False,
	mop_type: str = "Bank",
) -> dict:
	return {
		"name": name,
		"channel": channel,
		"code": code,
		"form": form or ("БЕЗГОТІВКОВА" if allow_cashless else "ІНШЕ"),
		"allow_cashless": int(allow_cashless),
		"allow_other": int(allow_other),
		"allow_prepayment": int(allow_prepayment),
		"allow_debt": int(allow_debt),
		"requires_terminal": int(requires_terminal),
		"mop_type": mop_type,
	}


